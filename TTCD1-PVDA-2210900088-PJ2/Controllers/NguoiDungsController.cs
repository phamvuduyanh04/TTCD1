using System;
using System.Collections.Generic;
using System.Data;
using System.Data.Entity;
using System.Linq;
using System.Net;
using System.Web;
using System.Web.Mvc;
using TTCD1_PVDA_2210900088_PJ2.Models;

namespace TTCD1_PVDA_2210900088_PJ2.Controllers
